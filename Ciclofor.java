public class Ciclofor {
    public static void main(String[] args) {
         // Ciclo For

         for (int i = 0; i < 10; i++) {
            System.out.println("Este ciclo se a repetido: "+i+" veces");
        }

        for (int a = 0; a < 10; a++) {
            System.out.println(a);
        }

        for (int b = 0; b < 10; b++) {
            System.out.println("2 x "+b+" = "+(2*b));
        }
    }
}
